package main

import "fmt"

// функция высшего порядка, вернёт функцию,
// которая будет ожидать очередной аргумент
func add(a int) func(int) int {
	return func(b int) int {
		return a + b
	}
}

// ~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~
// ------------------------------- Клиентский код -------------------------------
// ~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~
func main() {
	fmt.Println(add(1)(2))
}
