package main

import "fmt"

// ------------------------------------------------------------------------------
// ------------------------------------------------------------------------------
// Curring (Каррирование)
// Цель: зафиксировать повторяющиеся аргументы при вызове функции, получив
// новую функцию
func add(a int) func(int) int {
	return func(b int) int {
		return a + b
	}
}

func main() {

	fmt.Println(add(1)(2))

}
